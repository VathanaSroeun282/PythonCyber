text = "Hello, kon papa";

#upper case
print(text.upper())

#lower case
print(text.lower())

#remove white space 
print(text.strip())

#replace string
print(text.replace('H', 'GG'))

#split string to list
arr_text = text.split(" ")
print(arr_text)

#find index of the value 
print(text.find('world'))

#count string 
print(text.count('Hello'))